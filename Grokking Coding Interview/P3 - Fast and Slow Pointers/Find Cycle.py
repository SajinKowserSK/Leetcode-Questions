class Node:
  def __init__(self, value, next=None):
    self.value = value
    self.next = next


def has_cycle(head):
  slow = head
  fast = head

  ahead = 0
  while fast.next != None and ahead < 2:
      fast = fast.next
      ahead += 1

  if fast.next is None: # reached end, return False bc regular linked list
      return False

  while fast.next.next != None:
      slow = slow.next
      fast = fast.next.next

      if slow == fast:
          return True

  return False


def main():
  head = Node(1)
  head.next = Node(2)
  head.next.next = Node(3)
  head.next.next.next = Node(4)
  head.next.next.next.next = Node(5)
  head.next.next.next.next.next = Node(6)
  print("LinkedList has cycle: " + str(has_cycle(head)))

