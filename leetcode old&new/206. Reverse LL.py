class Solution(object):
    def reverseList(self, head):
        prev = None
        curr = head

        return self.reverse(prev, curr)

    def reverse(self, prev, curr):
        if curr is None:
            return prev

        next_node = curr.next
        curr.next = prev
        prev = curr
        curr = next_node
        return self.reverse(prev, curr)


